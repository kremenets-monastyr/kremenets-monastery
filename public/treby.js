function isWarrior(name){
  const s=String(name||'').toLowerCase().trim();
  if(!s)return false;
  if(/(^|[\s,;(])в\.(\s|$)/.test(s))return true;
  return /(во[іїй]н|воин|войн|б[іо][йє]ц|військовослужб|воєннослужб|безв[іе]ст|полонен|полонян|зниклий|зсу|всу)/.test(s);
}
