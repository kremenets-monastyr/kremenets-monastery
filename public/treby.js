function isLongTerm(title){ return /(3\s*місяц|6\s*місяц|1\s*рік|5\s*рок)/i.test(String(title||'')); }
function trebaLong(s){ const tr=trebaOf(s); return !!(tr && (tr.long || isLongTerm(tr.t))); }
